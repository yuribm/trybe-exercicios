const a = 6;
const b = 3;

// primeiro programa, adição (a + b)

let resultadoAdicao = a + b;
console.log(resultadoAdicao);

// segundo programa, subtração (a - b)

let resultadoSubtracao = a - b;
console.log(resultadoSubtracao);

// terceiro programa, multiplicação (a * b)

let resultadoMultiplicacao = a * b;
console.log(resultadoMultiplicacao);

// quarto programa, divisão (a / b)

let resultadoDivisao = a / b;
console.log(resultadoDivisao);

// quinto programa, módulo (a % b)

let resultadoModulo = a % b;
console.log(resultadoModulo);